package com.harkon.jdocs;

public class JDocsException extends RuntimeException {

	public JDocsException() {
		super();
	}

	public JDocsException(String arg0) {
		super(arg0);
	}

	public JDocsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public JDocsException(Throwable arg0) {
		super(arg0);
	}

}
