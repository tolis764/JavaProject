package com.harkon.jdocs;

public class JDocsRuntimeException extends RuntimeException {

	public JDocsRuntimeException() {
		super();
	}

	public JDocsRuntimeException(String message) {
		super(message);
	}

	public JDocsRuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	public JDocsRuntimeException(Throwable cause) {
		super(cause);
	}

}
